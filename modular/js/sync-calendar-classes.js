/* Claude: 2026-04-09 — Sync calendar classes to studentConfig quizzes
   Runs on page load (Study Tools): if calendar has classes like "HORT 328",
   auto-create empty quiz documents so they show as available classes in Study Tools */

window.syncCalendarClassesToQuizzes = function() {
  if (!window.AA || !window.AA.auth || !window.AA.db) {
    console.warn('[AA] Sync skipped: Firebase not ready');
    return;
  }
  
  var uid = window.AA_MIRROR_UID || window.AA.auth.currentUser.uid;
  console.log('[AA] Starting calendar→quizzes sync for uid:', uid);
  
  // Step 1: Load calendar class schedule
  window.AA.db.collection('users').doc(uid).get()
    .then(function(doc) {
      if (!doc.exists) {
        console.warn('[AA] User doc not found:', uid);
        return;
      }
      
      var classes = doc.data().classSchedule;
      if (!classes || !classes.length) {
        console.log('[AA] No calendar classes found');
        return;
      }
      
      console.log('[AA] Found calendar classes:', classes.map(function(c) { return c.code; }));
      
      var classMap = {}; // classCode → class object
      
      classes.forEach(function(cls) {
        classMap[cls.code] = cls;
      });
      
      // Step 2: Load current quizzes from studentConfig
      return window.AA.db.collection('studentConfig').doc(uid).get()
        .then(function(snap) {
          if (!snap.exists) {
            console.warn('[AA] studentConfig not found');
            return;
          }
          var config = snap.data();
          var quizzes = config.quizzes || [];
          
          console.log('[AA] Current quizzes:', quizzes.length);
          
          // Find which class codes are already in quizzes
          var existingClasses = new Set();
          quizzes.forEach(function(q) {
            var title = q.title || '';
            var match = title.match(/^([A-Z]{2,}\s*\d{3,})/);
            if (match) existingClasses.add(match[1].trim());
          });
          
          console.log('[AA] Existing class codes:', Array.from(existingClasses));
          
          // Step 3: Create placeholder quizzes for missing classes
          var newQuizzes = [];
          for (var code in classMap) {
            if (!existingClasses.has(code)) {
              var cls = classMap[code];
              newQuizzes.push({
                title: code + ': Class Quizzes',
                type: 'flashcards',
                description: 'Quizzes for ' + (cls.name || code),
                data: [],
                trimester: 'spring-2026',  // Claude: 2026-04-09 — organize into Spring section
                createdAt: new Date(),
                autoGenerated: true
              });
            }
          }
          
          console.log('[AA] Creating', newQuizzes.length, 'new quizzes');
          
          // Step 4: If there are new quizzes, add them to studentConfig
          if (newQuizzes.length > 0) {
            var updatedQuizzes = quizzes.concat(newQuizzes);
            return window.AA.db.collection('studentConfig').doc(uid).update({
              quizzes: updatedQuizzes
            }).then(function() {
              console.log('[AA] Auto-created ' + newQuizzes.length + ' class quizzes from calendar');
            });
          } else {
            console.log('[AA] All classes already have quizzes');
          }
        });
    })
    .catch(function(err) {
      console.error('[AA] Calendar → quizzes sync failed:', err);
    });
};

// Run sync when page loads
var _syncPoll = setInterval(function() {
  if (window.AA && window.AA.auth && window.AA.db) {
    clearInterval(_syncPoll);
    window.syncCalendarClassesToQuizzes();
  }
}, 250);
